<h3>8) Draft: The OBHL Board will review the self-ratings of each registered player and make rating adjustments</h3>
<p>as necessary.</p>
<p style="margin-left:1.5em;"><strong>a)</strong> Player Trades: The OBHL Board reserves the right to trade players among teams at any point during</p>
<p>the season - team rosters are never final and all players are eligible to be traded. Although the intent</p>
<p>of the board is to never trade players, the bigger goal for the league is parity among teams. Therefore,</p>
<p>it may be necessary to move players between teams as new players are added to the division during</p>
<p>the season, or to realign skills between teams to maintain competitive balance.</p>
<p style="margin-left:1.5em;"><strong>b)</strong> Same-team Requests: The OBHL Board will attempt to place players together on a team that</p>
<p>requests so during the registration process. However, there are no guarantees that such a request</p>
<p>will be fulfilled. Multi-player, same-team requests have become extremely difficult to honor, as the</p>
<p>board attempts to balance player skills and distribute our player-referees and general managers</p>
<p>among the various teams. Therefore, any same-team requests to be considered by the board shall</p>
<p>consist of only pairs - no more than two players - and each of the two in such a pair must request</p>
<p>each other during the registration process. If only one person in the pair makes the request, the</p>
<p>board will not attempt to place the players together.</p>
<p style="margin-left:1.5em;"><strong>c)</strong> Player Attitude: Those players, by majority vote of the OBHL Board, having attitudes that do not</p>
<p>match what is desired for the league will not be placed in the league.</p>
<p style="margin-left:1.5em;"><strong>d)</strong> Consensus: Once the &quot;pool&quot; of players is determined, the OBHL Board will draft equal teams by</p>
<p>consensus (i.e., drafting teams together rather than as separate GMs). Typically, a draft skate will</p>
<p>be held to assess new players and determine an appropriate skill rating for each. The board will</p>
<p>review and adjust player ratings for the pool of players prior to dividing players into teams. The</p>
<p>draft process is not a true &quot;draft&quot; where GMs choose specific players. Instead, the process is</p>
<p>conducted such that the league talent is evenly distributed between teams in an attempt for</p>
<p>parity. GMs do not select their teams or players, but are assigned as dictated by their own ability</p>
<p>and balancing other factors such as spreading adequate defenders over all the teams, distributing</p>
<p>referees over all the teams, meeting same-team requests, and other factors.</p>
