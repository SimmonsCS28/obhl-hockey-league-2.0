<h3>10) Playing Rules: Although games are not USA Hockey sanctioned events, OBHL Division games adhere to the</h3>
<p>current edition of Official Rules of Ice Hockey, as published by USA Hockey, except as otherwise noted herein.</p>
<h3>11) Hat Trick Rule (Three-Goal Limit): The league has a three goal per player per game limit. In addition, the</h3>
<p>OBHL Board may designate certain players who are subject to a limit of less than three goals per game.</p>
<p>(Generally, this will be a 2-goal limit applied to players with an OBHL Board skill rating of 9 or above.) Any</p>
<p>goal over a player&#x27;s limit will not count towards the team score or the individual player&#x27;s statistics, and the</p>
<p>play will result in an ensuing face-off to be held in the offending team&#x27;s defensive zone. Exception: If, in the</p>
<p>game officials&#x27; judgment, the goalie did not make a good-faith effort to stop the puck from entering the net,</p>
<p>the face-off will be held in the zone where the play occurred.</p>
<p>11a) Free Goal Limit</p>
<p>In the event that a team is ahead by 4 goals or greater, all players regardless of rating on the losing team have</p>
<p>their goal limits completely removed. This stays in effect until the losing team is within 2 goals or fewer of the</p>
<p>winning team. At this time all normal goal limits are reinstated and any goals scored during the catchup</p>
<p>period count towards that players&#x27; normal goal limit total. This means if a two goal scorer scores two goals</p>
<p>during the catchup period, when normal limits are reinstated they will not be able to score because they&#x27;d</p>
<p>now be at their two-goal limit. In the event that the score differential becomes 4 goals or greater again, the</p>
<p>goal limits are removed once more until the score differential is within 2 goals or fewer.</p>
<h3>12) Face-Off Location: Upon a stoppage of play, the game officials will first determine the proper zone in which</h3>
<p>to hold the ensuing face-off. The face-off will then be held on the nearest face-off dot to the location of</p>
<p>action that led to the stoppage of play.</p>
<h3>13) Captains: GMs for each team are the designated Captains. As Captains, the GMs have the privilege to discuss</h3>
<p>interpretations of rules with the Officials. GMs missing a game shall designate a Captain in his or her</p>
<p>absence. The designated Captain must then report to the score table and to the officials working the game.</p>
<p>Game officials are encouraged to assess penalties (i.e., minor for unsportsmanlike conduct, misconduct, or</p>
<p>game misconduct) to any player - including a designated Captain - who acts in a hostile manner or</p>
<p>otherwise verbally challenges the authority of an Official.</p>
<h3>14) Penalties: The decision of On-Ice Officials is final. This is a NO-CHECK, adult hockey league! Officials will not</h3>
<p>always be the same each week - some officials may be more lenient than others. However, the objective</p>
<p>remains the same: &quot;Everyone in the league has a job they must perform away from the rink! Players are</p>
<p>expected to play under control, which includes not jeopardizing anyone&#x27;s livelihood.&quot; Note: Penalty data will</p>
<p>be recorded on score sheets and kept by the OBHL Board and arena management for potential future</p>
<p>disciplinary action, as discussed herein.</p>
<h3>15) Standards of Discipline: At a minimum, the following specific penalty guidelines will be used as standards of</h3>
<p>discipline:</p>
<p style="margin-left:1.5em;"><strong>a)</strong> No Check Hockey: The league is &quot;NO-CHECK.&quot; Checking will result in a minor penalty with additional</p>
<p>10-minute misconduct or a major penalty and game disqualification if the checking is from behind. b)</p>
<p>Time of Penalties: Any minor or major penalty assessed during running time will be 3:00 minutes and</p>
<p>6:00 minutes, respectively. Any minor or major penalty assessed during stop time will be 2:00 minutes</p>
<p>and 5:00 minutes, respectively.</p>
<p style="margin-left:1.5em;"><strong>c)</strong> 3-Penalty Rule: Any player who receives three penalties in a single game will be disqualified for the</p>
<p>balance of that game and the next regularly scheduled game so long as none of the three penalties</p>
<p>assessed are a &quot;game misconduct&quot; penalty. The 3-penalty rule is intended to minimize personal fouls</p>
<p>and abuse of officials. Any player receiving three penalties in a game must remove himself/herself</p>
<p>from the game and go to the locker room. Any player failing to do so and having it later discovered</p>
<p>that three penalties were received must sit the next two games.</p>
<p style="margin-left:1.5em;"><strong>d)</strong> 4-Penalty/2-Game Rule: A player who gets a total of four penalties in two back-to-back games in</p>
<p>which he or she participates will be suspended for one game. This rule does not negate the 3-penalty</p>
<p>in a single game rule nor is it an add-on to the 3-penalty rule. (Clarification example: If a player has</p>
<p>one penalty in game one and three penalties in game two (back-to-back), then the player would be</p>
<p>removed from game two because of committing 3 penalties in one game. He or she would then be</p>
<p>required to sit one more game (due to the 3-Penalty Rule) but not a second game because of also</p>
<p>reaching the &quot;4-Penalty/2-Game&quot; rule).</p>
<p style="margin-left:1.5em;"><strong>e)</strong> Subsequent suspensions for 3-in-one-game or 4-in-two-games penalties: A player who multiple</p>
<p>times in one season receives 3 penalties in one game, or 4 penalties in two games, as described</p>
<p>above, shall be subject to the following automatic discipline:</p>
<p style="margin-left:3em;">i. Second occurrence: disqualification from current game and suspension for the next three (3)</p>
<p>regularly scheduled games.</p>
<p style="margin-left:3em;">ii. Third (or more) occurrence: disqualification from current game and suspension for the next five</p>
<p>(5) regularly scheduled games; vote of confidence by the OBHL Board to determine if the player</p>
<p>will be allowed to continue playing in the league.</p>
<p style="margin-left:1.5em;"><strong>f)</strong> Game Misconduct Penalties: Any player who is assessed a game misconduct will be ejected from the</p>
<p>game immediately and will be suspended for the next two regularly scheduled games as well. g) Major</p>
<p>Penalties: Any player who is assessed two major penalties during a game will be ejected from the game</p>
<p>immediately and will be suspended for the next three regularly scheduled games as well. h) Match</p>
<p>Penalties: Any player who is assessed a match penalty in accordance with USA Hockey Rules may not</p>
<p>skate in any league games until after appearing before league management and SP Ice Management to</p>
<p>determine appropriate discipline. This meeting (i.e., appearance before league and arena management)</p>
<p>must take place within thirty days of game occurrence. The minimum standard for a Match Penalty</p>
<p>within the OBHL shall be two games. After a hearing, the disciplinary committee</p>
<p>shall determine whether more than a two-game suspension is necessary. However, keep in mind that</p>
<p>the disciplinary committee is under no obligation to meet prior to thirty days of game occurrence. i)</p>
<p>Multiple Suspensions: Any player who is suspended twice in a single season will be removed from the</p>
