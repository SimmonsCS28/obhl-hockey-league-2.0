<h2>OBHL GAME RULES</h2>
<h3>1) Game Start Times: Game start times listed on schedules are approximate and may occasionally change due to</h3>
<p>games that run long or short. There are times when a game may start a few minutes later than scheduled</p>
<p>and other times when a game may start slightly earlier than scheduled. Please arrive early and be patient if a</p>
<p>game is running late. The schedule is posted on the website at sunprairieice.com.</p>
<h3>2) Game Format: Each game will start with a 5-minute warm-up period followed by three, 20-minute running</h3>
<p>time periods. The clock will stop on a whistle during the final three minutes of the third period or at any</p>
<p>other time when the Officials determine it is appropriate to do so. The clock will not be stopped during the</p>
<p>final three minutes of the third period if either team leads by four or more goals.</p>
<h3>3) Game Forfeit: Any team with less than seven skaters suited for a game may skate the game competitively</h3>
<p>with the players on hand, or they may choose to forfeit the contest and use subs from other teams registered</p>
<p>for the current OBHL season. This rule is in place so that a competitive game can still be played even though</p>
<p>the game is recorded as a forfeit. (Note: Subs from outside the league are not permitted, even in this</p>
<p>instance).</p>
<h3>4) Time Out: Time outs are not permitted.</h3>
<h3>5) Tie Regular Season Games: Regular season games ending in a tied score after regulation will be settled by one</h3>
<p>3-minute, stop-clock, sudden-victory overtime (OT) period (5 players each team). If the game is still tied after</p>
<p>the OT period, the game is recorded as a tie with each team receiving 1 point.</p>
<h3>6) Tie Playoff Games: Playoff games ending in a tied score after regulation are followed by one, 3-minute, stop</h3>
<p>clock, sudden-victory OT period. If the game remains tied after the overtime, the game is settled by a 3-man</p>
<p>shootout. If the game is still tied after the 3-man shootout, the game is settled by successive 1-man</p>
<p>shootouts until a winner is determined.</p>
<h3>7) Shootout Rules (Playoffs only)</h3>
<p style="margin-left:1.5em;"><strong>a)</strong> Any player in the penalty box at the end of overtime cannot participate in the shootout. b) No</p>
<p>player on either team may shoot a second time in the same game until all eligible shooters on the</p>
<p>team with the fewest eligible shooters suited for the game have participated in the shootout. c) Any</p>
<p>player who has reached his or her goal limit for the game (see below) by the end of overtime cannot</p>
<p>participate in the shootout and does not count as an eligible shooter for purposes of the previous</p>
<p>rule.</p>
<p style="margin-left:1.5em;"><strong>d)</strong> Teams are required to rotate shootout participants from game to game, such that all team</p>
<p>members must have the opportunity to participate in a shootout before any team member can</p>
<p>participate for a second time. If a team violates this rule during a game, the team will forfeit any</p>
<p>points it would otherwise have earned for such game.</p>
